	<p>The town was first mentioned as "Ursella" in 791 in a deed recording a donation to the monastery Lorsch. In the year 880 the first church in the area, called "monasterium ad ursellam", was mentioned. In 1317 the first trading activities were documented. In 1444 Oberursel was officially sanctioned as a town. Two years later, the town wall was finished. Since the town was growing so fast in the 15th century the town walls had to be expanded in 1481 towards the east. In the early 16th century a school for Latin language was established next to the church.</p>

	<p>1535 Oberursel become property of Earl Ludwig von Stolberg-KÃ¶nigstein and becomes a Protestant town. 1557 Nicolaus Henricus built the first printing plant in Oberursel. 1605 due to the counter-reformation Oberursel becomes Catholic again. The most important industry in the late Middle Ages and in the early modern times was the clothier business.</p>

	<p>During the Thirty Years' War the town was destroyed three times, in 1622 and 1645, and the number of inhabitants decreased from 1,600 to 600. During the 1645 razing, most buildings were destroyed by fire; the three surviving buildings were the old town hall, the bakery, and one apartment house near the marketplace. This is the reason why nearly all houses in the old town of Oberursel are from the 17th century.</p>

