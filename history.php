	<p>As the town was rebuilt and grew, many mills and forges were founded along the "Urselbachtal". By 1858, industrialisation had begun in Oberursel, with several cotton-spinning mills being built. In 1860 Oberursel was linked to the railway between Bad Homburg and Frankfurt. In 1899 a subway was opened privately in the direction of Hohemark (currently Line U3).</p>


	<p>During the Second World War, many captured American and British airmen passed through Oberursel as they were interrogated and processed into the German POW camp system at the "Durchgangslager der Luftwaffe" or "Transit Camp of the Luftwaffe" located in the town. The camp name was commonly shortened to "Dulag Luft", or simply "Dulag". Almost all allied airmen who were shot down and captured spent some time at that camp before being moved to their permanent assigned "Stalag".</p>

	<p>After the occupation of Oberursel by the U.S. in April 1945, the "Dulag" area was taken over by the US Army. The existing buildings were transformed into Camp King, which began as an intelligence post, then later a transportation post until its de-activation in 1993. Today the former Camp King is a residential area, still populated by several former US soldiers.</p>
